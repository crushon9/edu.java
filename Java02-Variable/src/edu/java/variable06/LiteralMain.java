package edu.java.variable06;

public class LiteralMain {

	public static void main(String[] args) {
		System.out.println("리터럴(Literal)");
		int integer1 = 1243; // 정수 리터럴은 기본적으로 int(4byte)
		long integer2 = 123235435L; // long 타입 뒤에 l 또는 L을 추가
		// long 타입의 리터럴로 선언됨
		
		double decimal1 = 3.1415; // 실수 리터럴은 기본적으로 double(8yte)
		float decimal2 = 3.14f; // float 타입은 뒤에 f 또는 F를 추가
		
		char ch1 = 'A'; // 문자 리터럴은 기본적으로 char(2byte)
		String str1 = "문자열"; // 문자열 리터럴은 기본적으로 String
		
	} // end main()
} // end LiteralMain
